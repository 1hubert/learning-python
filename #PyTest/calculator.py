def square_root(x: float) -> float:
    """Return square root of a given number."""
    return x ** (1/2)

def division_by_n(x: float, n: float) -> float:
    """Return quotient of x and n.

    Args:
        x (float): A real number
        n (float): A real number

    Returns:
        float: Quotient of x and n.
    """
    return x / n